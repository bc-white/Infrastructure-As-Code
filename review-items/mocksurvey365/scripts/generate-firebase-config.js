#!/usr/bin/env node
/*
  Generates public/firebase-config.js from environment variables.
  Reads both VITE_* and plain FIREBASE_* keys to be flexible.
*/
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';
import dotenv from 'dotenv';

// Load .env if present
dotenv.config();

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const publicDir = path.join(projectRoot, 'public');
const outFile = path.join(publicDir, 'firebase-config.js');

function pickEnv(key) {
  return (
    process.env[key] ||
    process.env[`VITE_${key}`] ||
    process.env[key.toLowerCase()] ||
    process.env[`VITE_${key.toLowerCase()}`] ||
    ''
  );
}

const config = {
  apiKey: pickEnv('FIREBASE_API_KEY'),
  authDomain: pickEnv('FIREBASE_AUTH_DOMAIN'),
  projectId: pickEnv('FIREBASE_PROJECT_ID'),
  storageBucket: pickEnv('FIREBASE_STORAGE_BUCKET'),
  messagingSenderId: pickEnv('FIREBASE_MESSAGING_SENDER_ID'),
  appId: pickEnv('FIREBASE_APP_ID'),
  measurementId: pickEnv('FIREBASE_MEASUREMENT_ID'),
};

// Basic validation
const required = ['apiKey', 'authDomain', 'projectId', 'messagingSenderId', 'appId'];
const missing = required.filter((k) => !config[k]);
if (missing.length) {
 // eslint-disable-next-line no-console
}

// Ensure public dir exists
fs.mkdirSync(publicDir, { recursive: true });

const fileContent = `// Generated by scripts/generate-firebase-config.js\nself.__FIREBASE_CONFIG__ = ${JSON.stringify(config)};\n`;

try {
  fs.writeFileSync(outFile, fileContent, 'utf-8');
 
} catch (err) {
  
  process.exit(1);
}
